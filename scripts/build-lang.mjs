function walk(dir) {
  for (const file of fs.readdirSync(dir)) {
    const full = path.join(dir, file)
    if (fs.statSync(full).isDirectory()) {
      walk(full)
      continue
    }
    if (!file.endsWith('Module.java')) continue
    const text = fs.readFileSync(full, 'utf8')
    const match = text.match(/super\("([^"]+)",\s*"([^"]+)",\s*"([^"]*)"/)
    if (!match) continue
    const [, id, name, desc] = match
    const key = id.replace(/-/g, '_')
    en[`prime.module.${key}.name`] = name
    en[`prime.module.${key}.description`] = desc
    fr[`prime.module.${key}.name`] = name
    fr[`prime.module.${key}.description`] = frDesc[desc] ?? desc
  }
}

